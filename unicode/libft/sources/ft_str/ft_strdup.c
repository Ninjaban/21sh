#include "libft.h"

char	*ft_strdup(const char *src)
{
	char			*dest;
	unsigned int	i;

	if (!src)
		return (NULL);
	if ((dest = (char *)malloc(sizeof(char) * (ft_strlen(src) + 1))) != NULL)
	{
		i = 0;
		while (src[i])
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (dest);
}
